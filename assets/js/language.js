// Set user's preferred language (default to English)
let currentLanguage = localStorage.getItem('language') || 'en';
document.documentElement.lang = currentLanguage;
document.documentElement.dir = currentLanguage === 'ar' ? 'rtl' : 'ltr';

// Update text content based on selected language
function updateLanguage(lang) {
    currentLanguage = lang;
    localStorage.setItem('language', lang);

    // Set language attribute and text direction
    document.documentElement.lang = lang;
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';

    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            element.textContent = translations[lang][key];
        }
    });

    // Special handling for placeholder attributes
    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        if (translations[lang] && translations[lang][key]) {
            element.placeholder = translations[lang][key];
        }
    });
}

// Show language indicator
function showLanguageIndicator(lang) {
    const indicator = document.getElementById('languageIndicator');
    if (!indicator) return;

    // Remove existing language classes
    indicator.className = 'language-indicator';

    // Add the language-specific class
    indicator.classList.add(lang);

    // Show language name in indicator
    const languageName = translations[lang] ? translations[lang][lang] : lang.toUpperCase();

    // Display the indicator
    indicator.classList.add('show');

    // Hide after 3 seconds
    setTimeout(() => {
        indicator.classList.remove('show');
    }, 3000);
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', () => {
    updateLanguage(currentLanguage);

    // Set selected language in dropdown if it exists
    const langSelector = document.getElementById('language-selector');
    if (langSelector) {
        langSelector.value = currentLanguage;
    }

    // Check if we need to show the indicator
    if (localStorage.getItem('showIndicator') === 'true') {
        // Clear the flag
        localStorage.removeItem('showIndicator');

        // Show indicator for current language
        showLanguageIndicator(currentLanguage);
    }
});

// Function to change language
function changeLanguage(lang) {
    // Save previous language for comparison
    const prevLang = currentLanguage;

    // Update language
    updateLanguage(lang);

    // Only show indicator if language actually changed
    if (prevLang !== lang) {
        // We'll show it after page reload
        localStorage.setItem('showIndicator', 'true');
    }

    // Reload page for complete language refresh
    // This ensures all dynamically generated content is also translated
    window.location.reload();
}
